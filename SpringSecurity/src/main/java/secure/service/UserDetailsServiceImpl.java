package secure.service;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;
import secure.entity.User;
import secure.entity.enums.UserRoleEnum;

import java.util.HashSet;
import java.util.Set;

/**
 * Created by Dell on 6/15/2015.
 */
@Service
public class UserDetailsServiceImpl implements UserDetailsService {

    @Autowired
    private UserService userService;

    @Override
    public UserDetails loadUserByUsername(String s) throws UsernameNotFoundException {
        // ? ??????? ????? ??????? UserService ??????? User
        User user = userService.getUser("colibri");
        // ????????? ???? ??? ????? ????????????
        Set<GrantedAuthority> roles = new HashSet<>();
        roles.add(new SimpleGrantedAuthority(UserRoleEnum.USER.name()));
        // ?? ????????? ??????????? ????? ????????? ?????? UserDetails
        // ??????? ???????? ????????? ???????? ????????????? ????? ? ??????
        // ? ??? ????? ????????????????? ????????????
        UserDetails userDetails = new org.springframework.security.core.userdetails.User(user.getLogin(),user.getPassword(),roles);

        return userDetails;
    }
}
